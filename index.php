<b> Asssassin Creed Series</b>
                     <p align="justify">Assassin's Creed is an action-adventure stealth video game franchise created by Patrice Désilets, Jade Raymond and Corey May, developed and published by Ubisoft using the game engine Anvil Next. It depicts in the centuries-old struggle, now and then, between the Assassins, who fight for peace with free will, and the Templars, who desire peace through control. The series features historical fiction, science fiction and characters, intertwined with real-world historical events and figures. For the majority of time players would control an Assassin in the past history, while they also play as Desmond Miles or an Assassin Initiate in the present day, who hunt down their Templar targets.

<p align="justify">The video game series took inspiration from the novel Alamut by the Slovenian writer Vladimir Bartol,[1] while building upon concepts from the Prince of Persia series.[2] It begins with the self-titled game in 2007, and has featured eleven main games. The most recent released game is 2018's Assassin's Creed Odyssey.</p>

<p align="justify">A new story and time period are introduced in each entry, and gameplay elements evolve from the previous one. There are three story arcs in the series. For the first five main games, the framing story is set in 2012 and features series protagonist Desmond Miles who uses a machine called the Animus and relives the memories of his ancestors to find a way to avert the 2012 apocalypse. In games till Assassin's Creed Syndicate, Abstergo employees and Assassin initiates recorded genetic memories using the Helix software, helping the Templars and Assassins find new Pieces of Eden in the modern world. The latest two games, Assassin's Creed Origins and Assassin's Creed Odyssey follow ex-Abstergo employee Layla Hassan as she is recruited into the Assassin's Creed.</p>

<p align="justify">Main games of Assassin's Creed are set in an open world and presented from the third-person perspective where the protagonists take down targets using their combat and stealth skills with the exploitation of the environment. Freedom of exploration is given to the player the historical settings to finish main and side quests. Apart from single-player missions, some games also provide competitive and cooperative multiplayer gameplay. While main games are produced for major consoles and desktop platforms, multiple spin-off games were also released in accompany for consoles, mobiles, and handhelds platforms.</p>

